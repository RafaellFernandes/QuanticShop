    DEVOLUÇÃO

----

Nossa política de devolução é de 30 dias. Passados os 30 dias da compra, infelizmente não é possível solicitar reembolso ou troca.

Para poder efetuar a devolução, o produto não pode ter sido usado e deve estar nas mesmas condições em que você o recebeu. Ele também deve estar na embalagem original.

Vários tipos de mercadorias estão isentos de devolução. Produtos perecíveis, tais como alimentos, flores, jornais ou revistas não podem ser devolvidos. Nós também não aceitamos produtos íntimos ou sanitários, materiais perigosos, líquidos ou gases inflamáveis.

Itens adicionais que não podem ser devolvidos:
* Cartões-presente
* Produtos de software para download
* Alguns itens de higiene pessoal e de saúde

Para efetuar a devolução, apresente o comprovante ou comprovante de compra.

Por favor, não envie sua compra de volta ao fabricante.

Em certos casos, serão emitidos apenas reembolsos parciais (quando aplicável):
* Livros com sinais óbvios de uso
* CDs, DVDs, fitas VHS, software, videogames, fitas cassete ou discos de vinil que tenham sido abertos.
* Qualquer produto que não esteja em sua condição original, que esteja danificado ou com partes faltando, não sendo devido a qualquer erro de nossa parte.
* Qualquer produto que for devolvido mais de 30 dias após a entrega

Reembolsos (quando aplicável)
Uma vez que o produto para devolução for recebido e verificado, enviaremos um e-mail para notificá-lo que recebemos sua mercadoria para devolução. Também vamos notificá-lo sobre a aprovação ou rejeição do seu reembolso.
Caso seja aprovado, o seu reembolso será processado. Emitiremos o crédito automaticamente no seu cartão de crédito ou método de pagamento da compra em alguns dias.

Reembolso atrasado ou não recebido (quando aplicável)
Se você ainda não recebeu o reembolso, verifique sua conta bancária novamente.
Em seguida, entre em contato com sua empresa de cartão de crédito, já que pode demorar algum tempo até o reembolso ser lançado.
Depois entre em contato com o seu banco. Em geral, há um tempo processamento antes do lançamento de um reembolso.
Se você já fez tudo isso e ainda não recebeu o reembolso, por favor, entre em contato conosco pelo raphaelldff@gmail.com.

Mercadorias em oferta (quando aplicável)
Você só pode pedir o reembolso de produtos com preço normal. Infelizmente, não é possível solicitar reembolso de mercadorias em oferta.

Trocas (quando aplicável)
Só trocamos produtos que vierem com defeito ou danificados. Se você precisar trocar um item pelo mesmo produto, envie um e-mail para raphaelldff@gmail.com e envie o seu produto para: Av.Paraná, Umuarama, PR, 87502-000, Brazil.

Presentes
Se o produto foi marcado como presente durante a compra e foi enviado diretamente para você, você receberá um vale-presente com o mesmo valor da compra. Você receberá um vale-presente pelo correio quando recebermos o produto devolvido.

Se o produto não foi marcado como presente durante a compra, ou caso quem o presenteou enviou o produto para si mesmo para entregá-lo pessoalmente, enviaremos um reembolso a quem deu o presente. Essa pessoa vai saber que você solicitou a devolução.

Frete
Para fazer a devolução do produto, você deve enviá-lo para: Av.Paraná, Umuarama, PR, 87502-000, Brazil.

Você será responsável pelo pagamento do frete para devolver o produto. Não reembolsamos o frete. Se você receber um reembolso, o custo do frete de devolução será deduzido do seu reembolso.

Dependendo de onde você mora, o tempo de frete de devolução pode variar.

Se você vai enviar um produto acima de $75, considere contratar um serviço de frete com rastreamento ou seguro. Não podemos garantir o recebimento do produto devolvido.
